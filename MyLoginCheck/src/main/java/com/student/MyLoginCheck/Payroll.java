package com.student.MyLoginCheck;

public class Payroll {
	
	private String EmpCode;
	private float Basic;
	private float DA;
	private float HRA;
	private float CCA;
	private float pf;
	private float ptax;
	public String getEmpCode() {
		return EmpCode;
	}
	public void setEmpCode(String empCode) {
		EmpCode = empCode;
	}
	public float getBasic() {
		return Basic;
	}
	public void setBasic(float basic) {
		Basic = basic;
	}
	public float getDA() {
		return DA;
	}
	public void setDA(float dA) {
		DA = dA;
	}
	public float getHRA() {
		return HRA;
	}
	public void setHRA(float hRA) {
		HRA = hRA;
	}
	public float getCCA() {
		return CCA;
	}
	public void setCCA(float cCA) {
		CCA = cCA;
	}
	public float getPf() {
		return pf;
	}
	public void setPf(float pf) {
		this.pf = pf;
	}
	public float getPtax() {
		return ptax;
	}
	public void setPtax(float ptax) {
		this.ptax = ptax;
	}
	

}
