package com.student.MyLoginCheck;

public class LeaveBalance {
	
	private String EmpCode;
	private float TotalLeave;
	private float LeaveAvailed;
	private float BalanceLeave;
	public String getEmpCode() {
		return EmpCode;
	}
	public void setEmpCode(String empCode) {
		EmpCode = empCode;
	}
	public float getTotalLeave() {
		return TotalLeave;
	}
	public void setTotalLeave(float totalLeave) {
		TotalLeave = totalLeave;
	}
	public float getLeaveAvailed() {
		return LeaveAvailed;
	}
	public void setLeaveAvailed(float leaveAvailed) {
		LeaveAvailed = leaveAvailed;
	}
	public float getBalanceLeave() {
		return BalanceLeave;
	}
	public void setBalanceLeave(float balanceLeave) {
		BalanceLeave = balanceLeave;
	}
	
	

}
